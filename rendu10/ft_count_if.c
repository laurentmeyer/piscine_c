int		ft_count_if(char **tab, int (*f)(char *))
{
	int i;
	int acc;

	i = 0;
	acc = 0;
	while (tab[i] != 0)
	{
		if (f(tab[i]) == 1)
			acc++;
		i++;
	}
	return (acc);
}
