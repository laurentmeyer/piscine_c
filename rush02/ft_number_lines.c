#include "ft_rush.h"

int		ft_number_lines(char *str)
{
	int i;
	int acc;

	i = 0;
	acc = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '\n')
			acc++;
		i++;
	}
	return (acc);
}
