void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	if (*str != 0)
	{
		ft_putchar(*str);
		ft_putstr(str + 1);
	}
	return ;
}

int		main(int argc, char **argv)
{
	int i;

	i = argc - 1;
	while (i > 0)
	{
		ft_putstr(argv[i]);
		ft_putchar('\n');
		i--;
	}
	return (0);
}
